                     <p>Veliko nam je zadovoljstvo pozvati te na WebCamp Zagreb 2013, jednodnevnu konferenciju posvećenu web programerima, koja će se održati 26. listopada 2013. u Zagrebu. No ovo nije običan poziv – pozivamo te da nam pomogneš u realizaciji konferencije vlastitim predavanjem! Jer uz izvrsnu priliku za networking, vjerojatno najvažniji dio svake konfererencije su stručna predavanja. Ako izuzmemo besplatne pizze.</p>
                     <p>Posebnost konferencije WebCamp leži upravo u činjenici da ju je pokrenula i realizirala zajednica – organizatori, predavači i sudionici.</p>
                     <p>Organizatore čine korisničke grupe <a href="http://codeatsix.infinum.hr/meetup" title="Code@Six">Code@Six</a>, <a href="http://www.frontman-hr.org" title="Frontman HR">FrontmanHr</a>, <a href="http://www.mscommunity.hr/" title="Future DEV">FutureDEV</a>, <a href="http://croatia.girlgeekdinners.com/" title="Girl Geek Dinners Hrvatska">Girl Geek Dinners Hrvatska</a>, <a href="http://hujak.hr/" title="HUJAK">HUJAK</a>, <a href="https://twitter.com/jszgb" title="JavaScript Zagreb">Javascript Zagreb</a>, <a href="https://groups.google.com/forum/?fromgroups=#!forum/python-hr">Python Hrvatska</a> i <a href="http://zgphp.org/" title="ZgPHP">ZgPHP</a>, dok ćemo sudionike tek pozvati da (kao prošle godine) razgrabe karte u nekoliko dana. Ali prije trebamo okupiti najvažniju skupinu – predavače.</p>
                     <p>Tu ulijećeš ti. Treba nam tvoje predavanje. Ili više njih. Prijavi sve ideje koje imaš, možda nam se ukusi razlikuju ili je neka od tema već pokrivena. Nije nam bitno kojom se tehnologijom bavi, sve su dobrodošle. Neka bude zanimljiva i aktualna. Sve što ima veze s webom i razvojem, makar i nategnuto. Posebno volimo iskustva iz prve ruke.</p>
                     <p>Predavanja trebaju biti tehničke, a ne promotivne prirode - svakako nastojite uključiti dobre i loše strane i prikazati stvari objektivno, a konkretne projekte treba pokazati iznutra, a ne iz perspektive običnog korisnika. Ukratko rečeno: pričamo o tehnologijama, a ne marketing.</p>
                     <p>Nemaš ideju? Evo par općenitih natuknica: arhitektura aplikacija, hosting, cloud, metodologije razvoja softvera, testiranje aplikacija, alati za razvoj, dizajn, klijentski i serverski razvoj, web za mobilne uređaje, predstavljanje specifičnih tehnologija, baze podataka i one koje to nisu, hejtanje tehnologije koju volite i koristite, upravljanje web-projektima, kolaboracijski sustavi, distribuirani timski rad…</p>
                     <p>Sva predavanja će biti u trajanju od 25 minuta.</p>
                     <p>Prijaviti se možete putem <a href="2013.webcampzg.org/prijava-predavanja/" title="Prijavi predavanje za WebCamp Zagreb">WebCamp prijavnice za predavače</a>. Krajnji rok za prijave je do 9.9. u 24h.</p>
                     <p>Izbor tema i predavača vrši se glasanjem svih voditelja korisničkih grupa koje sudjeluju u organizaciji konferencije. Svi odabrani predavači bit će obaviješteni osobno, e-mailom do kraja rujna (vjerojatno i prije).</p>
